export class Agregado {

    restaurante: string;
    id: number;
    nombre: string;
    costo: number;

    

    constructor(restaurante: string, id: number, nombre: string, costo: number)
        {
           this.restaurante = restaurante;
           this.id = id;
           this.nombre = nombre;
           this.costo = costo;
     
    }

}
